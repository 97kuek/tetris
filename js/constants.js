// ゲーム全体で使用される定数を管理する

const COLS = 10; // 盤面の列数
const ROWS = 20; // 盤面の行数
const BLOCK_SIZE = 30; // 1ブロックのサイズ(ピクセル単位)

// テトリミノの色を定義する配列。
// インデックスがミノの色IDに対応します（pieces.jsのcolorIdと連携）。
// COLORS[0] は null で、空のセルを表します。
const COLORS = [
    null,      // 0: 空のセル
    '#00f0f0', // 1: Iミノ (シアン)
    '#0000f0', // 2: Jミノ (ブルー)
    '#f0a000', // 3: Lミノ (オレンジ)
    '#f0f000', // 4: Oミノ (イエロー)
    '#00f000', // 5: Sミノ (グリーン)
    '#a000f0', // 6: Tミノ (パープル)
    '#f00000'  // 7: Zミノ (レッド)
];

/**
 * ゲームのレベルに応じてミノの落下速度（インターバル）を返します。
 * @param {number} level - 現在のゲームレベル。
 * @returns {number} - 落下インターバル（ミリ秒）。数値が小さいほど速い。
 */
function getDropInterval(level) {
    // レベルが20を超えた場合は、最高速度（20ms）を維持します。
    if (level > 20) return 20;

    // レベルごとの落下速度を配列で定義(簡易的)
    const speeds = [
        1000, // L1: 1秒
        900,  // L2
        800,  // L3
        720,  // L4
        650,  // L5
        580,  // L6
        520,  // L7
        460,  // L8
        410,  // L9
        360,  // L10
        320,  // L11
        280,  // L12
        240,  // L13
        200,  // L14
        160,  // L15
        130,  // L16
        100,  // L17
        80,   // L18
        60,   // L19
        40    // L20
    ];

    return speeds[level - 1] || 20; // レベルに対応する落下速度を返すまたは、最高速度を返す
}

